export function getItemStory(item: PortfolioItem): string {
  const stories = categoryStories[item.category];
  const idx = portfolioItems.findIndex((p) => p.id === item.id);
  return stories[idx % stories.length];
}

export function getItemGalleryImages(item: PortfolioItem): string[] {
  const sameCategory = portfolioItems.filter(
    (p) => p.category === item.category && p.id !== item.id
  );
  if (sameCategory.length === 0) return [];
  const idx = portfolioItems.findIndex((p) => p.id === item.id);
  const offset = idx % Math.max(sameCategory.length - 2, 1);
  const pool = [...sameCategory.slice(offset), ...sameCategory.slice(0, offset)];
  return pool.slice(0, 4).map((p) => p.image);
}

export function getRelatedItems(
  item: PortfolioItem,
  count = 3
): PortfolioItem[] {
  const sameCategory = portfolioItems.filter(
    (p) => p.category === item.category && p.id !== item.id
  );
  const idx = portfolioItems.findIndex((p) => p.id === item.id);
  const offset = (idx + 1) % Math.max(sameCategory.length, 1);
  return [...sameCategory.slice(offset), ...sameCategory.slice(0, offset)].slice(
    0,
    count
  );
}

export function getAdjacentItems(item: PortfolioItem) {
  const idx = portfolioItems.findIndex((p) => p.id === item.id);
  return {
    prev: idx > 0 ? portfolioItems[idx - 1] : null,
    next: idx < portfolioItems.length - 1 ? portfolioItems[idx + 1] : null,
  };
}
